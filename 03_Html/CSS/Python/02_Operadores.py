### OPERADORES ###

'''Operadores aritméticas'''''
print (8+6)# Suma   
print (8-6) # Resta
print (8*6) # Multiplicación
print (8**6) # Potencia
print (8**0.5) # Raíz cuadrada
print (8**(1/6)) # Raíz sexta
print (8/6) # División normal
print (8%6)# Módulo
print (8//6) # División entera

print ('Hola' + " Me llamo Steven") # Concatenación de cadenas
print ('Hola' * 3) # Repetición de cadenas
print ('Hola' * 3 + " Me llamo Steven") # Repetición de cadenas y concatenación
print ('Hola' * (3 + 2)) # Repetición de cadenas y concatenación
print ('Hola' * (3 + 2) + 'Me llamo Steven') # Repetición de cadenas y concatenación
print ('Hola' * (3 + 2) + 'Me llamo Steven' + str(8)) # Repetición de cadenas y concatenación
print ('Hola' * (3 + 2) + 'Me llamo Steven' + str(8) + str(6)) # Repetición de cadenas y concatenación

'''Operadores de comparación'''
print (8 == 6) # Igual que
print (8 != 6) # Diferente que
print (8 > 6) # Mayor que
print (8 < 6) # Menor que
print (8 >= 6) # Mayor o igual que
print (8 <= 6) # Menor o igual que

print ('Hola' > "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' < "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' == "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' != "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' >= "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' <= "Bye") # Comparación de cadenas (caracter por carácter)
print ('Hola' == "Hola") # Comparación de cadenas (caracter por carácter)
print ('Hola' != "Hola") # Comparación de cadenas (caracter por carácter)

'''OPERADORES LÓGICOS'''
print (8 > 5 and "Hola" < "Bye") # Y lógico
print (8 > 5 or "Hola" < "Bye") # O lógico
print (not (8 > 5)) # Negación lógica
print (not (8 < 5)) # Negación lógica
print (not (8 == 5)) # Negación lógica




